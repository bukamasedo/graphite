// Case-insensitive substring filter that works with Japanese and all Unicode characters.
// cmdk's default fuzzy filter doesn't handle CJK characters properly.
function unicodeFilter(
  value: string,
  search: string,
  keywords?: string[]
): number {
  if (!search) return 1;
  const s = search.toLowerCase();
  const candidates = [value, ...(keywords ?? [])];
  return candidates.some((c) => c.toLowerCase().includes(s)) ? 1 : 0;
}

const Command = React.forwardRef<
  React.ComponentRef<typeof CommandPrimitive>,
  React.ComponentPropsWithoutRef<typeof CommandPrimitive>
>(({ className, filter, ...props }, ref) => (
  <CommandPrimitive
    ref={ref}
    filter={filter ?? unicodeFilter}
    className={cn(
      'flex h-full w-full flex-col overflow-hidden rounded-lg bg-popover text-popover-foreground',
      className
    )}
    {...props}
  />
));
Command.displayName = CommandPrimitive.displayName;

